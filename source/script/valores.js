function somar(a, b) {
    return a + b;
}

function multi(c, d) {
    return c * d;
}

function divid(e, f) {
    return e + f;
}

function sub(g, h) {
return g + h;
}
 
function nome(pessoa) {
    return pessoa;
}


module.exports = somar;
module.exports = multi;
module.exports = divid;
module.exports = sub;
module.exports = nome;


